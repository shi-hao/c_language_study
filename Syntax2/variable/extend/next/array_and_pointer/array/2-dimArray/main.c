/*一维二维数组测试
 *@一维数组的定义、初始化、赋值、引用
 *@二维数组的定义、初始化、赋值、引用
 */
#include<stdio.h>
void main()
{
	/*定义*/
	int array1[3]={1,2,3};//初始化
	int array2[2][3]={1,2,3,4,5,6};//初始化,按行连续赋值
	int array3[2][3]={{1,2,3},{4,5,6}};//初始化,按行分段赋值
	int array4[][3]={{1,2,3},{4,5,6}};//初始化,按行分段赋值

	/*一维数组*/
	printf("array1[0]=%d\n",array1[0]);//引用

	/*二维数组*/
	printf("array2[0][2]=%d\n",array2[0][2]);//引用
	printf("array3[0][2]=%d\n",array3[0][2]);//引用
	printf("array4[0][2]=%d\n",array4[0][2]);//引用
}
